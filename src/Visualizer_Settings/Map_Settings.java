package Visualizer_Settings;

public class Map_Settings {

    private int x;
    private int y;
    private int block_percentage;
    public Map_Settings() {
        this.x = 10;
        this.y =10;
        this.block_percentage = 10;
    }

    public int getBlock_percentage() {
        return block_percentage;
    }

    public void setBlock_percentage(int block_percentage) {
        this.block_percentage = block_percentage;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }





}
